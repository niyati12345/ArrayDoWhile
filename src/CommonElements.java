public class CommonElements {
    public static void main(String args[]){

        String[] name= {"sej","niy","dev","isha"};
        String[] anam= {"niy","nish","f","sara","nita",};
        for(int i=0;i<name.length;i++){
            for(int j=0;j<anam.length;j++){
                if(name[i]==anam[j]){
                    System.out.println("the common element is: " +name[i]);
                }

        }




    }
}}
